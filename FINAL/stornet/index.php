<h1>StorNet: Co-Scheduling Network and Storage with TeraPaths and SRM</h1>
<p>Design and development of an integrated end-to-end resource provisioning and management system
for high performance data transfer framework to leverage heterogeneous network protocols and
storage types in a federated computing environment. The research challenge is to develop analytical
tools and efficient approaches for joint allocation and co-scheduling of well-balanced network and
storage resources involved in data transfer. The research outcomes will play a transformative role to
bridge end-to-end advanced storage and network technologies with science applications in a
transparent way.</p>
