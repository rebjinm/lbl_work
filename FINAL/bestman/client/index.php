<h1>BeStMan SRM-Client Tools User's Guide</h1>
<p>BeStMan SRM-Client tools are full implementations of SRM v2.2 as generic SRM v2.2 clients, developed by Lawrence Berkeley National Laboratory. They have been tested for all current SRM v2.2 implementations such as BeStMan, BeStMan2, CASTOR, dCache, DPM, SRM/iRODS-SRB and StoRM. They are continuously being tested for compatibility and interoperability. <br> It&rsquo;s a java implementation that would run on any OS platforms, and currently supports gsiftp, http, https and ftp as its file transfer protocols. It requires the minimal administrative efforts on the deployment and maintenance. <br> SRM v2.2 specification can be found on http://sdm.lbl.gov/srm-wg/doc/SRM.v2.2.html.</p>
