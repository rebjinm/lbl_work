	<h2>Goals</h2>
	<ul>
		<li>Developing high performance I/O middleware that makes effective use of exascale computing and I/O resources</li>
		<li>Improving the productivity of application developers by hiding the complexity of parallel I/O </li>
		<li>Facilitating scientific analysis for users by extending query-based techniques</li>
	</ul>
	
	<h2>Key Ideas</h2>
	<ul>
		<li>Performance optimizations and transparent tuning features in HDF5 library that enable large scale runs</li>
		<li>Development of easy-to-use data models that improve productivity of scientists and hide complex I/O hardware and middleware</li>
		<li> Leveraging state-of-the-art indexing and querying technologies to dramatically accelerate analysis procedures</li>
	</ul>
	
