<h1>Berkeley Storage Manager version 3 (BeStMan3)</h1>
<p>BeStMan3 is a full implementation of <a href="https://sdm.lbl.gov/twiki/bin/view/Projects/SRMwg/WebHome">SRM v2.2</a>, developed by Lawrence Berkeley National Laboratory, for disk based storage systems and mass storage systems such as HPSS. It is based on the BeStMan2 in its functionalities, and adds more reliability and scalability along with more plug-in features. Like the previous BeStMan and BeStMan2, it works on top of existing disk-based unix file system, and has been reported so far to work on file systems such as NFS, PVFS, AFS, GFS, GPFS, PNFS, and Lustre. It also works with any existing file transfer service, such as gsiftp, http, https and ftp. It requires minimal administrative efforts on the deployment and maintenance.</p>
