<h1>Earth System Grid (ESG) Center for Enabling Technologies</h1>
<p>DOE SciDAC-2 Earth System Grid Center for Enabling Technologies (ESG-CET) project is to provide climate
researchers worldwide with access to: data, information, models, analysis tools, and computational resources
required to make sense of enormous climate simulation datasets.</p>
<p>As scientists intensify their dependence on more sophisticated next-generated coupled atmospheric-ocean-
sea-ice-land models to predict future climate change, greater need is also placed on the management of
climate data and resources. Central to the current and future dissemination of climate data is the Earth
System Grid Center for Enabling Technologies (ESG-CET)—a DOE SciDAC-2 project established to build a
“science gateway” to climate resources that provides data, information, models, analysis, and visualization
tools and computational capabilities for management and analysis.</p>
<p>The ESG-CET project’s goals are: (1) to make data more useful to climate researchers by developing Grid
technology that enhances data usability; (2) to meet specific needs which national and international climate
projects have for distributed database, data access, and data movement; (3) to provide a universal and
secure web-based data access portal for broad-based multimodel data collections; and (4) to provide a wide-
range of Grid-enabled climate data analysis tools and diagnostic methods to international climate centers and
U.S. government agencies. Thus, ESG-CET is working to integrate distributed data and computers, high-
bandwidth wide-area networks, and remote computing using climate data analysis tools in a highly
collaborative problem-solving environment.</p>
