<h1>Bulk Data Movement</h1>
<p>Bulk Data Mover (BDM) is a scalable data transfer management tool for GridFTP transfer protocol. The goal is to manage as much as 1+ PB with millions of files transfers reliably. </p>
<p>Bulk Data Movement is an <a href="https://sdm.lbl.gov/esg">ESG</a> (<a href="http://www.earthsystemgrid.org/">http://www.earthsystemgrid.org</a>) project.</p>

