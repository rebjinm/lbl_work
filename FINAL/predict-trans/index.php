	<h1>Predicting State Transitions by Learning Spatio-Temporal Patterns</h1>
	<p><strong>Investigator:</strong> Alex Sim</p>
	
	<h2>Scientific Achievement</h2>
	<p>Newly developed analysis methods learn spatio-temporal relationship on streaming data, and reveal transitions in network operation states, offering a new way to classify and predict the anomalous state.</p>
	
	<h2>Significance and Impact</h2>
	<p><We developed novel analytical approaches towards autonomous systems, which produce better understanding of the variability in multivariate features and handling high frequency of the streaming data collection for analysis, event classification and
prediction./p>
	
	<h2>Research Details</h2>
	<ul>
		<li>Our research studied machine learning-based clustering method and density-based grid structure and joint distribution methods to understand variations in patterns for data with multiple features.</li>
		<li>Similarity measures have been designed to estimate temporal variations; "degree of change" based on moving distance of clusters and "common occupancy rate‚Äù (similarity) based on the concept of Jaccard Index for grid structure.</li>
		<li>Streaming data including network traffic monitoring measurements show the non-linear property, and the dimensionality reduction is a challenge. Deep learning models would be essential to find spatio-temporal variations in data streams with multiple features.</li>
	</ul>
