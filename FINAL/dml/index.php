<h1>DataMover-Lite (DML)</h1>
<p>DataMover-Lite (DML) is a simple file transfer tool with graphical user interface which supports multi-protocol data movement. DML is available in both webstart and standalone version.</p>
<p>Currently, DML supports http, https, ftp, gridftp, lahfs and scp. For GridFTP, DML also supports directory browsing and transferring.</p>
<p>The GUI provides two ways for generating a proxy credential required for the data transfer:</p>
<ul>
	<li>Creating a proxy credential using a locally stored key pair.</li>
	<li>Obtaining a proxy from a MyProxy Server.</li><br>
</ul>
<p>Prereqquisites:<p>
<ul>
<li>JDK 1.5.0+</li>
</ul>
<p>Supported Platforms:</p>
<ul>
	<li>Unix/Linux</li>
	<li>Windows</li>
	<li>MAC</li>
</ul>

